package main

import "fmt"

func main() {
	x := 10
	fmt.Printf("Valor de x: %d\n", x)
	fmt.Printf("Valor em Binario: %b\n", x)
	fmt.Printf("Valor em Hexadecimal: %#x\n", x) //%#x usado por convenção para apresentar o valor em hexadecimal
}

